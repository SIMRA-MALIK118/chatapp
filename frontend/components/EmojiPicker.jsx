export default function EmojiPicker({ onEmojiSelect, onClose }) {
  const [search, setSearch] = useState('');
  const [selectedCategory, setSelectedCategory] = useState('recent');
  const ref = useRef(null);

  useEffect(() => {
    const handler = (e) => {
      if (ref.current && !ref.current.contains(e.target)) onClose?.();
    };
    document.addEventListener('mousedown', handler);
    return () => document.removeEventListener('mousedown', handler);
  }, [onClose]);

  const allEmojis = CATEGORIES.flatMap(c => c.emojis);
  const currentEmojis = search.length > 0
    ? allEmojis.filter((_, i) => i % 3 === 0 || search.length > 1).slice(0, 80)
    : CATEGORIES.find(c => c.id === selectedCategory)?.emojis || [];

  return (
    <div ref={ref} className="absolute bottom-full mb-2 bg-[#1a1a2e] border border-[#2a2a45] rounded-2xl shadow-2xl p-3 z-50 w-80">
      {/* Search */}
      <div className="flex items-center gap-2 mb-3 pb-3 border-b border-[#2a2a45]">
        <Search className="w-4 h-4 text-gray-500 flex-shrink-0" />
        <input
          type="text"
          placeholder="Search emoji..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          autoFocus
          className="flex-1 bg-[#111128] border border-[#2a2a45] rounded-lg px-2 py-1.5 text-xs text-white placeholder-gray-600 focus:outline-none focus:border-purple-500"
        />
      </div>

      {/* Emoji Grid */}
      <div className="mb-3 max-h-56 overflow-y-auto pr-1">
        <div className="grid grid-cols-7 gap-0.5 w-full">
          {(search.length > 0
            ? allEmojis.filter((e, i, arr) => arr.indexOf(e) === i).slice(0, 70)
            : currentEmojis
          ).map((emoji, i) => (
            <button
              key={`${emoji}-${i}`}
              onClick={() => {
                onEmojiSelect(emoji);
                setSearch('');
                onClose?.();
              }}
              className="text-xl hover:bg-[#2a2a45] p-1.5 rounded transition active:scale-110 flex items-center justify-center"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>

      {/* Category tabs */}
      <div className="flex gap-0.5 pt-2 border-t border-[#2a2a45] justify-between overflow-x-auto">
        {CATEGORIES.map((cat) => (
          <button
            key={cat.id}
            onClick={() => { setSelectedCategory(cat.id); setSearch(''); }}
            className={`text-base p-1.5 rounded-lg transition flex-shrink-0 ${
              selectedCategory === cat.id && search.length === 0
                ? 'bg-purple-600/30 text-white'
                : 'text-gray-400 hover:text-white hover:bg-[#2a2a45]'
            }`}
            title={cat.label}
          >
            {cat.icon}
          </button>
        ))}
      </div>
    </div>
  );
}
